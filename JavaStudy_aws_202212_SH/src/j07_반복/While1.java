package j07_반복;

public class While1 {

	public static void main(String[] args) {
		//for문과 while문의 비교
		
		for(int i = 0; i < 10; i++) {
			System.out.println(i);
		}
		
		System.out.println("==================");
		
		int i = 0;
		
		while(i < 10) {
			System.out.println(i);
			
			i++;
		}

	}

}
